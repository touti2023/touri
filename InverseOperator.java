


public class InverseOperator {
	
	public static void main(String[] agrs) {
		
		
		//  !取反  和　^逻辑异或 案例演示
		
		// ！操作是T-》F；F--》T
		System.out.println(60 > 20);//T
		System.out.println(!(60 > 20));//F
		
		
		//a^b 叫逻辑异或，当a 和 b 不同时则为T，否则为F。
		boolean b = (10 > 1)^ ( 3 > 5);
		System.out.println("b=" + b);//T
		
		
		
		
		
		
	     }
	}